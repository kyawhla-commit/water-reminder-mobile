export const getArticlesByCategory = (category?: string): HealthArticle[] => {
  if (!category || category === 'all') return HEALTH_ARTICLES;
  return HEALTH_ARTICLES.filter(a => a.category === category);
};

export const getArticleById = (id: string): HealthArticle | undefined => {
  return HEALTH_ARTICLES.find(a => a.id === id);
};

export const getDailyArticle = (): HealthArticle => {
  const dayOfYear = Math.floor((Date.now() - new Date(new Date().getFullYear(), 0, 0).getTime()) / 86400000);
  return HEALTH_ARTICLES[dayOfYear % HEALTH_ARTICLES.length];
};
